public class NarowFloat 
{
    public static void main(String[]arg){

        float f = 3.14f;

        byte b =(byte)f;
        short s =(short)f;
        int i = (int)f;
        char ch = (char)f;
        long l = (long)f;

        double d = f;

        System.out.println(f);
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(ch);
        System.out.println(l);
        System.out.println(d);
    }
    
}
